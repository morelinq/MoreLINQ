namespace MoreLinq
{
    /// <summary>
    /// Enumeration that defines values representing valid ordering directions for a sequence.
    /// </summary>
    public enum OrderByDirection
    {
        /// <summary>
        /// Elements are ordered by increasing value
        /// </summary>
        Ascending = 0,
        /// <summary>
        /// Elements are ordered by decreasing value
        /// </summary>
        Descending = 1,
    }
}